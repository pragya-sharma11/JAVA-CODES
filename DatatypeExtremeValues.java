public class DatatypeExtremeValues {
    public static void main(String[] args) {
        System.out.println("Minimum Value of Byte: "+Byte.MIN_VALUE+" and Maximum Value of Byte: "+Byte.MAX_VALUE);
        System.out.println("Minimum Value of Short: "+Short.MIN_VALUE+" and Maximum Value of Short: "+Short.MAX_VALUE);
        System.out.println("Minimum Value of Integer: "+Integer.MIN_VALUE+" and Maximum Value of Integer: "+Integer.MAX_VALUE);
        System.out.println("Minimum Value of Long: "+Long.MIN_VALUE+" and Maximum Value of Long: "+Long.MAX_VALUE);
        System.out.println("Minimum Value of Float: "+Float.MIN_VALUE+" and Maximum Value of Float: "+Float.MAX_VALUE);
        System.out.println("Minimum Value of Double "+Double.MIN_VALUE+" and Maximum Value of Double: "+Double.MAX_VALUE);
        System.out.println("Minimum Value of Char: "+(int)Character.MIN_VALUE+" and Maximum Value of Char: "+(int)Character.MAX_VALUE);
    }
}
